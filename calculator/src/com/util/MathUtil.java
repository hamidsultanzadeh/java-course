package com.util;

public class MathUtil {

    public static double topla(double a, double b){
        return a+b;
    }

    public static double chix(double a, double b){
        return a-b;
    }

    public static double vur(double a, double b){
        return a*b;
    }

    public static double bol(double a, double b){
        return a/b;
    }
}
